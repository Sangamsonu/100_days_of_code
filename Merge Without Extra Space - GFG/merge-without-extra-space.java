class Solution
{
    //Function to merge the arrays.
     static void swap(long a1[],long a2[],int i,int j){
        long t=a1[i];
        a1[i]=a2[j];
        a2[j]=t;
   
    }
    
    public static void merge(long a1[], long a2[], int n, int m) {
        int lt=n-1,rt=0;
        while(lt>=0 && rt<m){
            if(a1[lt]>a2[rt]){
                swap(a1,a2,lt,rt);
                lt--;rt++;
            }
            else{
                break;
            }
        }
        Arrays.sort(a1);
        Arrays.sort(a2);
    }
}
    
